    public static void assertEqualsList(List<Game> expected, List<Game> actual) {
        for (Game cExpected : expected) {
            boolean gameInList = isGameInList(cExpected, actual);

            if(!gameInList) {
                System.out.println("Error with game: " + cExpected.getName());
            }

            assertEquals(true, gameInList);
        }

        for (Game cActual : actual) {
            boolean gameInList = isGameInList(cActual , actual);

            if(!gameInList) {
                System.out.println("Error with game: " + cActual.getName());
            }

            assertEquals(true, gameInList);
        }
    }

    public static boolean assertEqualsListBoolean(List<Game> expected, List<Game> actual) {
        for (Game cExpected : expected) {
            boolean gameInList = isGameInList(cExpected, actual);

            if(!gameInList) {
                return false;
            }
        }

        for (Game cActual : actual) {
            boolean gameInList = isGameInList(cActual , actual);

            if(!gameInList) {
                return false;
            }
        }

        return true;
    }

    public static boolean isGameInList(Game needle, List<Game> haystack) {
        for (Game cGame : haystack) {
            if (gameEquals(cGame, needle)) {
                return true;
            }
        }

        return false;
    }

    public static int gameInListCount(Game needle, List<Game> haystack) {
        int count = 0;

        for (Game cGame : haystack) {
            if (gameEquals(cGame, needle)) {
                count++;
            }
        }

        return count;
    }

    public static boolean gameEquals(Game a, Game b) {
        return a.getName().equals(b.getName()) &&
                dateEquals(a.getReleaseDate(), b.getReleaseDate()) &&
                doubleEquals(a.getPrice(), b.getPrice());
    }

    public static boolean doubleEquals(double a, double b) {
        return Math.abs(a - b) < 0.5;
    }

    public static boolean dateEquals(Date a, Date b) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");

        String dateStringA = sdf.format(a);
        String dateSTringB = sdf.format(b);

        return dateStringA.equals(dateSTringB);
    }

